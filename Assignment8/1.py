"""FOR"""
n = int(input("Enter number of days: "))
total = 0
for i in range(1, n + 1):
    total = total + i

print("Total Points =", total)


"""WHILE"""
n = int(input("Enter number of days: "))
point=0
i=1
while i<=n:
      point=point+i
      i+=1
print("Total Points =", point)



